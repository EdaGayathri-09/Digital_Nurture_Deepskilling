public class TypeCast {
    public static void main(String args[]){
        //Double to int casting
        double num1=45.57;
        int num2=(int)num1;

        //Int to double casting
        int num3=25;
        double num4=(double)num3;

        System.out.println("Original double value: "+num1);
        System.out.println("Double converted to int: "+num2);
        System.out.println("Original int value: "+num3);
        System.out.println("Int converted to double: "+num4);
    }
}
